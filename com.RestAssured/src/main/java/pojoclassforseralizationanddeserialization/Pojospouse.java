package pojoclassforseralizationanddeserialization;

public class Pojospouse {
	
	String ename;
	String eid;
	public Pojospouse(String ename, String eid) {
		super();
		this.ename = ename;
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getEid() {
		return eid;
	}
	public void setEid(String eid) {
		this.eid = eid;
	}
	

}
